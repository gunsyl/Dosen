using System;

namespace Dosen {
    public class DosenKu
    {
        private string nama;
        private string nik;
        private string gender;
        private string course;

        public DosenKu(string nama, string nik, string gender, string course)
        {
            this.nama = nama;
            this.nik = nik;
            this.gender = gender;
            this.course = course;
        }
    }

}
